func day3count(stepX, stepY int) int {
	input := strings.TrimSpace(day3input)
	inputArr := strings.Split(input, "\n")
	count := 0
	x := 0
	y := 0
	maxY := len(inputArr)
	maxX := len(inputArr[0])
	// . = 46; # = 35
	for y < maxY-1 {
		x += stepX
		y += stepY

		if x > maxX-1 {
			x -= (maxX)
		}

		if inputArr[y][x] == 35 {
			count++
		}

	}

	return count
}

func day3() int {
	return day3count(3, 1)
}

func day3part2() int {
	return day3count(1, 1) * day3count(3, 1) * day3count(5, 1) * day3count(7, 1) * day3count(1, 2)
}

